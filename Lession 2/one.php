1)    $3rdQuarter  = ไม่ถูก เพราะ มีตัวเลขขึ้นต้น
2)    $_1stQuarter  = ถูก ตามกฏข้อที่ 1
3)    $%complete  = ไม่ถูก เพราะ มีตัวแปรอื่นขึ้นต้น
4)    $complete%   = ถูก ตามกฏข้อที่ 3
5)    $your&name  = ไม่ถูก เพราะ มีตัวแปร & 
6)    $your name  = ไม่ถูก เพราะ มีช่องเว้นว่าง
7)    $MYNAME   = ถูก เพราะเป็นตัวอักษรเดียวกัน